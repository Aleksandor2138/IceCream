function amoreFunction() {
    var adots = document.getElementById('adots');
    var amore = document.getElementById('amore');
    var abtnOpen = document.getElementById('abtnOpen');
    var abtnClose = document.getElementById('abtnClose');

    if (adots.style.display === 'none') {
    adots.style.display = 'inline';
    abtnClose.style.display = 'none';
    abtnOpen.style.display = 'inline';
    amore.style.display = 'none';
    }
    else {
    adots.style.display = 'none';
    abtnOpen.style.display = 'none';
    abtnClose.style.display = 'inline';
    amore.style.display = 'inline';
    }
}

